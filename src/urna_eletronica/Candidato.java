    private void jButton12MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton12MouseClicked
        // TODO add your handling code here:
        Tela_bloqueio most = new Tela_bloqueio();
        most.Bozo = bozo;
        most.Dilma=Dilma;
        most.branco=branco;
        most.nulo=nulo;
        if (caixaTexto.getText().equals("12")){
            most.Dilma=Dilma + 1;
         // caixaTexto.setText("");
         // Limage.setIcon(null);
          // nome_candidato.setVisible(false);
           chamaAudio();
         
            JOptionPane.showMessageDialog(null,"Obrigado pelo voto");
            dispose();
           most.setVisible(true);
          
        }else if (caixaTexto.getText().equals("17")){
             most.Bozo=bozo + 1; 
         // caixaTexto.setText("");
         // Limage.setIcon(null);
         //  nome_candidato.setVisible(false);
           chamaAudio();
           JOptionPane.showMessageDialog(null,"Obrigado por jogar seu voto no lixo");
          dispose();
            most.setVisible(true); 
        
        }else if (nome_candidato.getText().equals("VOTO EM BRANCO"))  {
            most.branco=branco + 1;
             //  caixaTexto.setText("");
       //   Limage.setIcon(null);
         //  nome_candidato.setVisible(false);
           chamaAudio();
           JOptionPane.showMessageDialog(null,"Fim");
           dispose();
            most.setVisible(true);
        }else{
            
            most.nulo=nulo+1;
              // caixaTexto.setText("");
        //  Limage.setIcon(null);
         //  nome_candidato.setVisible(false);
           chamaAudio();
           JOptionPane.showMessageDialog(null,"Fim");
            dispose();
            most.setVisible(true);
           
        }      
       
      
    }//GEN-LAST:event_jButton12MouseClicked

